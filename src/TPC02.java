public class TPC02 {

	public static void main(String[] args) {
		// 프로그래밍의 3대 요소 : 변수, 자료형(data type), 할당(=)
		// 1+1=2 메모리라는 기억 공간이 필요
		// 기억공간을 대표하는 이름 : 변수 기억 공간은 크기, 어떤 종류의 데이터를 저장할 것인가가 고려되어야
		// 변수 선언해야 기억공간이 만들어진다
		int a, b, c; // 변수 선언
		a = 1;
		b = 1;
		c = a + b;
		System.out.println(c);

		float f; // float, double
		f = 34.5f;
		System.out.println(f);

		char d;
		d = 'A';
		System.out.println(d);

		boolean g;
		g = true; // false(거짓), true(참)
		System.out.println(g);

		// Book bk;

	}

}
